        <p class="font-semibold mb-1">Marking a month as paid</p>
        <p>Right-click any of the 12 month boxes to open a colour menu and mark it — e.g. green for "paid", yellow for "in progress", red for "overdue". Pick <span class="font-medium">✕</span> to clear a mark. The mark is just a visual note (payment tracking) — it doesn't change the amount.</p>
    </div>

    <div>
        <p class="font-semibold mb-1">How typing an amount fills the row</p>
        <p>What happens to the other months depends on the expense's <span class="font-medium">Type</span> (set via the row's Edit action):</p>
        <ul class="list-disc list-inside space-y-1 mt-1">
            <li><span class="font-medium">Monthly</span> — the typed amount fills that month through December; earlier months keep their own values.</li>
            <li><span class="font-medium">One-time</span> — the amount lands in that one month only; every other month resets to 0.</li>
            <li><span class="font-medium">Annual (AVR)</span> — the typed value is the yearly total; it's split evenly across all 12 months.</li>
            <li><span class="font-medium">Volume</span> — manual mode; only the month you edit changes.</li>
        </ul>
    </div>
